inline
bool IsReverse(ENa_strand s)
{
    // treat unknown as forward
    return (s == eNa_strand_minus  ||  s == eNa_strand_both_rev);
}


inline
bool SameOrientation(ENa_strand a, ENa_strand b)
{
    return IsReverse(a) == IsReverse(b);
}


